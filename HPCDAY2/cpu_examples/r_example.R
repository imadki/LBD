# Example R script

# Create a numeric vector
numbers <- c(2, 4, 6, 8, 10)

# Calculate the mean and standard deviation
mean_value <- mean(numbers)
sd_value <- sd(numbers)

# Print the results
cat("Numeric vector:", numbers, "\n")
cat("Mean:", mean_value, "\n")
cat("Standard Deviation:", sd_value, "\n")
